% ============================================
% MATLAB Example 3: Simple Operations
% ============================================
% Demonstrates basic MATLAB operations

% Square root
x = 16;
disp('Square root of 16:');
disp(sqrt(x));

disp('');

% Exponential
y = 2;
disp('exp(2):');
disp(exp(y));

disp('');

% Natural logarithm
z = 10;
disp('log(10):');
disp(log(z));

disp('');

% Power operation
base = 2;
exponent = 8;
result = base ^ exponent;
disp('2 ^ 8 =');
disp(result);
