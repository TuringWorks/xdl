% ============================================
% MATLAB Example 2: Trigonometric Functions
% ============================================
% Demonstrates MATLAB trig functions

% Test angles
angle1 = 0;
angle2 = 3.14159 / 4;  % 45 degrees
angle3 = 3.14159 / 2;  % 90 degrees

disp('Trigonometric Functions:');
disp('');

% Angle 1
disp('Angle = 0 radians:');
disp('  sin(0) =');
disp(sin(angle1));
disp('  cos(0) =');
disp(cos(angle1));
disp('  tan(0) =');
disp(tan(angle1));

disp('');

% Angle 2
disp('Angle = π/4 radians (45 degrees):');
disp('  sin(π/4) =');
disp(sin(angle2));
disp('  cos(π/4) =');
disp(cos(angle2));
disp('  tan(π/4) =');
disp(tan(angle2));

disp('');

% Angle 3
disp('Angle = π/2 radians (90 degrees):');
disp('  sin(π/2) =');
disp(sin(angle3));
disp('  cos(π/2) =');
disp(cos(angle3));
